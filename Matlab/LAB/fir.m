clc;
x=0:0.01:1*pi;
y=sin(2*pi*2*x);
subplot(2,2,1);
plot(x,y);
grid on;
xlim([0 3.0]);
xlabel('time');
ylabel('Amplitude');
y1=sin(2*pi*47*x);
subplot(2,2,2);
plot(x,y1);
grid on;
xlim([0 3.0]);
xlabel('time');
ylabel('Amplitude');
subplot(2,2,3);
y2=y+y1;
plot(x,y2);
ylim([-3 3]);
hold on;
y3=y2(2:1:length(y2));
y4=y2(1:1:length(y2)-1);
y5=y3 + y4;
plot(x(1:1:length(x)-1),y5,'k-','LineWidth',1.5);
grid on;
xlim([0 3.0]);
xlabel('time');
ylabel('Amplitude');
subplot(2,2,4);
plot(x(1:1:length(x)-1),y5,'k-','LineWidth',1.5);
grid on;
xlim([0 3.0]);
xlabel('time');
ylabel('Amplitude');